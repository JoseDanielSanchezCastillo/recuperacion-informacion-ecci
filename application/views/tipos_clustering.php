	<h2 class="display-3" >Tipos de Clustering</h2>
	<br>
		<div class="row">
		<div class="col-sm-6" style=" padding-right: 25px">
			<p align="justify">
				Existen diferentes tipologías de algoritmos de clustering, definidas por la propiedad discriminate del algoritmo que utilizamos para tipificar. Podemos categorizar los algoritmos de clustering, principalmente, por tres propiedades:
			</p>

			<p><b>Meta</b>: Cardinalidad de criterios discriminantes de los cluster generados.</p>
			<ul> 
				<li>Monotético: Un único criterio diferenciador.</li> 
				<li>Politético: Múltiples criterios diferenciadores.</li> 
			</ul> 
			<p >
				<b>Conjuntos</b>: Intersección de los cluster.
			</p>
			<ul> 
				<li>Hard clustering: Clusters son conjuntos disjuntos.</li> 
				<li>Soft clustering: Clusters son conjuntos no disjuntos.</li> 
			</ul> 
			<br>
			<p >
				<b>Estructura</b>: Distrubuciòn de los clusters en el espacio.
			</p>
			<ul> 
				<li>Jerárquicos: Hay clusters son subconjuntos de otros.</li> 
				<li>Planos: Todos los clusters están al mismo “nivel”.</li> 
			</ul> 
			<br>

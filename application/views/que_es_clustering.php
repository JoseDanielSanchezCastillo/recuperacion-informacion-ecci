	<h2 class="display-3" >¿Qué es Clustering?</h2>
	<br>
	<p class="text-muted mb-0">
		El clustering es un tipo de aprendizaje no supervisado especialmente útil para reconocer patrones de distribución en el espacio de un grupo de datos de los cuales no tenemos conocimientos conocimiento a priori. Es una técnica muy presente en la minería de datos,  pero también se puede encontrar aplicada a otros campo como la recuperación de la información.  
	</p>
	<br>
	<p class="text-muted mb-0">
		El aprendizaje que efectúa está basado en la observación y su objetivo es dividir los datos en subconjuntos por cercanía. Dicha cercanía  se puede definir mediante cualquier función que compare dos elementos de la colección de datos, por que puede representar lo que se quiera. Por ejemplo podemos medir la cercanía euclidiana entre dos sonidos, la cantidad de letras que compartan dos palabras o la similitud en de la distribución de uso de dos usuarios en una aplicación. 
	</p>
